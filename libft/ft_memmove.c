#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*temp;

	temp = ft_strnew(len);
	ft_memcpy(temp, src, len);
	ft_memcpy(dst, temp, len);
	ft_strdel(&temp);
	return (dst);
}
